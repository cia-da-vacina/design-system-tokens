// ---------- 7) Gallery data ----------
function flattenForGallery(raw) {
  const palettes = {};
  for (const [group, scale] of Object.entries(raw)) {
    if (typeof scale === "string") {
      palettes[group] = { base: scale };
    } else {
      palettes[group] = scale;
    }
  }
  return palettes;
}

function loadSemantic(mode) {
  const modules = [
    "background",
    "text",
    "border",
    "foreground",
    "button",
    "icons",
    "utility",
  ];
  const out = {
    inherit: "inherit",
    transparent: "transparent",
  };
  for (const mod of modules) {
    const obj = loadTsObject(
      path.join(ROOT, `src/themes/global/${mode}/colors/${mod}.ts`),
      { colors: rawColors },
    );
    Object.assign(out, obj);
  }
  // crm overlay
  const crm = loadTsObject(
    path.join(ROOT, `src/themes/global/${mode}/crmOverlay.ts`),
    { colors: rawColors },
  );
  Object.assign(out, crm);
  return out;
}

