// ---------- 2) Copy semantic color modules light/dark ----------
function copySemanticTheme(mode) {
  const fromDir = path.join(SENCON, `themes/global/${mode}/colors`);
  const toDir = path.join(ROOT, `src/themes/global/${mode}/colors`);
  ensureDir(toDir);
  const files = fs.readdirSync(fromDir).filter((f) => f.endsWith(".ts"));
  for (const file of files) {
    let src = read(path.join(fromDir, file));
    src = src
      .replace(
        /import\s+colors\s+from\s+['"]@tokens\/global\/colors['"];?/,
        `import colors from "../../../../tokens/global/colors";`,
      )
      .replace(
        /from\s+['"]@themes\/global\/[^'"]+['"]/g,
        (m) => {
          // not expected inside colors/* but keep safe
          return m;
        },
      );
    // Rewrite sibling imports in index.ts
    src = src.replace(
      /from\s+['"]@themes\/global\/(light|dark)\/colors\/(\w+)['"]/g,
      `from "./$2"`,
    );
    src = src.replace(
      /import\s+(\w+)\s+from\s+['"]@themes\/global\/(?:light|dark)\/colors\/(\w+)['"];?/g,
      `import $1 from "./$2";`,
    );
    write(path.join(toDir, file), src);
  }

  // Fix index.ts specifically (Sencon uses @themes paths)
  const indexPath = path.join(toDir, "index.ts");
  let indexSrc = read(indexPath);
  indexSrc = indexSrc
    .replace(
      /import\s+(\w+)\s+from\s+['"]@themes\/global\/(?:light|dark)\/colors\/(\w+)['"];?/g,
      `import $1 from "./$2";`,
    )
    .replace(
      /from\s+['"]\.\/(\w+)['"]/g,
      `from "./$1"`,
    );
  write(indexPath, indexSrc);

  // theme index
  const themeIndexFrom = path.join(SENCON, `themes/global/${mode}/index.ts`);
  let themeIndex = read(themeIndexFrom)
    .replace(/from\s+['"]@themes\/global\/[^'"]+['"]/g, `from "./colors"`)
    .replace(
      /import\s+colors\s+from\s+['"][^'"]+['"];?/,
      `import colors from "./colors";`,
    );
  // Sencon uses: import colors from '@themes/global/light/colors'
  themeIndex = read(themeIndexFrom).replace(
    /import\s+.*?from\s+['"][^'"]+['"];?/,
    `import colors from "./colors";`,
  );
  // Keep name customization
  if (mode === "light") {
    themeIndex = `import colors from "./colors";

const light = {
  name: "ciaVacinaLight",
  colors,
};

export default light;
`;
  } else {
    themeIndex = `import colors from "./colors";

const dark = {
  name: "ciaVacinaDark",
  colors,
};

export default dark;
`;
  }
  write(path.join(ROOT, `src/themes/global/${mode}/index.ts`), themeIndex);
}

