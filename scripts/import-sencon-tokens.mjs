function ensureDir(p) {
  fs.mkdirSync(p, { recursive: true });
}

function read(p) {
  return fs.readFileSync(p, "utf8");
}

function write(p, content) {
  ensureDir(path.dirname(p));
  fs.writeFileSync(p, content);
  console.log("wrote", path.relative(ROOT, p));
}

/** Strip TS-only bits so we can eval token object literals in Node. */
function toPlainModule(src, { replaceImport } = {}) {
  let s = src
    .replace(/\/\*[\s\S]*?\*\//g, "")
    .replace(/^\s*\/\/.*$/gm, "")
    .replace(/\bas const\b/g, "");
  if (replaceImport) s = replaceImport(s);
  if (/export\s+default\s+colors/.test(s) && /const colors\s*=/.test(s)) {
    s = s.replace(/export\s+default\s+colors;?\s*$/m, "module.exports = colors;");
  } else {
    s = s.replace(/export\s+default\s+/, "module.exports = ");
  }
  s = s.replace(/export\s+\{[^}]+\}[\s;]*/g, "");
  return s;
}

function loadTsObject(filePath, { colors } = {}) {
  let src = read(filePath);
  if (colors) globalThis.__SENCON_COLORS__ = colors;
  src = toPlainModule(src, {
    replaceImport: (s) =>
      s
        .replace(
          /import\s+colors\s+from\s+['"][^'"]+['"];?/g,
          "const colors = globalThis.__SENCON_COLORS__;",
        )
        .replace(
          /import\s+raw\s+from\s+['"][^'"]+['"];?/g,
          "const raw = globalThis.__SENCON_COLORS__;",
        )
        .replace(/import\s+[^;]+;/g, ""),
  });
  const tmp = path.join(ROOT, ".tmp-token-eval.cjs");
  fs.writeFileSync(tmp, src);
  try {
    delete require.cache[require.resolve(tmp)];
    return require(tmp);
  } finally {
    try {
      fs.unlinkSync(tmp);
    } catch {
      /* ignore */
    }
  }
}

